#Whitespaces can be used to organize outputs

#For Python Tabs
print ("This is a \t python tab")

#For Python Newlines
print ("This is a \n New line")

#For Printing Newlines without spaces
print ("This is a New line\nwithout spaces\n")

#Another Examples
print("\tProgramming Languages :\nPython\nC++\nJava\nSwift")

#For new lines with Tabs
print("\tProgramming Languages :\n\t\tPython\n\t\tC++\n\t\tJava\n\t\tSwift") 