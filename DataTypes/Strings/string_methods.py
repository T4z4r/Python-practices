# changing the string case
name="This is the world's best python course"
print ("--------------title()------------------")
# Method: title() ~ Each word starts with an upper case letter
print (name.title())
print ("----------------upper()----------------")
#Method" upper() - Each character is capitalized
print(name.upper())
print ("-----------------lower()---------------")
# Method" lower() - Each character is changed to lower case
print (name.lower())

